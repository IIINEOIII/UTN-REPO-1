#2) Escribir un programa que solicite su nota al usuario. Si la nota es mayor o igual a 6, deberá 
#mostrar por pantalla un mensaje que diga “Aprobado”; en caso contrario deberá mostrar el 
#mensaje “Desaprobado”.

# SOLICITUD DE NOTA

#INGRESAMOS UN VALOR A NUESTRA VATIABLE
nota = int(input("Ingrese su nota por favor: "))

#CREAMOS NUESTRA CONDICION IF
if nota >= 6:

#IMPRIMIMOS UN MENSAJE EN PATALLA PISITIVO 
    print("Aprobado")

#SI NO SE CUMPLE ESTA CONDICION
else:

#IMPRIMIRA UN MENSAJE EN PANTALLA NEGATIVO
    print("Desaprobado")
